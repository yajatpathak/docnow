import { DarkTheme, DefaultTheme, ThemeProvider } from "@react-navigation/native";
import { useFonts } from "expo-font";
import { Stack } from "expo-router";
import * as SplashScreen from "expo-splash-screen";
import { StatusBar } from "expo-status-bar";
import { useEffect } from "react";
import "react-native-reanimated";

import { useColorScheme } from "@/hooks/useColorScheme";

// Prevent splash screen from hiding before assets load
SplashScreen.preventAutoHideAsync();

export default function RootLayout() {
  const colorScheme = useColorScheme();
  const [loaded] = useFonts({
    SpaceMono: require("../assets/fonts/SpaceMono-Regular.ttf"),
  });

  useEffect(() => {
    if (loaded) {
      SplashScreen.hideAsync();
    }
  }, [loaded]);

  if (!loaded) {
    return null;
  }

  return (
    <ThemeProvider value={colorScheme === "dark" ? DarkTheme : DefaultTheme}>
      <Stack>
        {/* Main Tabs Navigation */}
        <Stack.Screen name="(tabs)" options={{ headerShown: false }} />

        {/* Corrected Sign Up Screens */}
        <Stack.Screen name="SignUp" options={{ title: "Sign Up" }} />
        <Stack.Screen name="doctor-signup" options={{ title: "Doctor Sign Up" }} />
        <Stack.Screen name="patient-signup" options={{ title: "Patient Sign Up" }} />
        <Stack.Screen name="LoginScreen" options={{ title: "Login Screen" }} />
        <Stack.Screen name="patientDashboard" options={{ title: "Patient Trial" }} />

        {/* Not Found Page */}
        <Stack.Screen name="+not-found" />
      </Stack>

      <StatusBar style="auto" />
    </ThemeProvider>
  );
}
